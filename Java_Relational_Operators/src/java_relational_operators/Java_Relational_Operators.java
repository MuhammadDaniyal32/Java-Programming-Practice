package java_relational_operators;

/**
 *
 * @author mdani
 */
public class Java_Relational_Operators {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        
          int a= 20;
        int b= 30;
        
        System.out.println("a == b =" + (a == b));
        System.out.println("a != b =" + (a != b));
        System.out.println("a > b =" + (a > b));
        System.out.println("a < b =" + (a < b));
        System.out.println("a >= b =" + (a >= b));
        System.out.println("a <= b =" + (a >= b));
    }
    
}
